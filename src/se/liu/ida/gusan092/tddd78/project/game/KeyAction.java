package se.liu.ida.gusan092.tddd78.project.game;

/**
 * Actions that a key can make in controller
 */
public enum KeyAction
{
    /**
     * The action of moving the player up
     */
    MOVE_UP,
    /**
     * The action of moving the player down
     */
    MOVE_DOWN,
    /**
     * The action of moving the player left
     */
    MOVE_LEFT,
    /**
     * The action of moving the player right
     */
    MOVE_RIGHT,
    /**
     * The action of using the players power ups
     */
    USE_POWERUPS
}
